"""Si un número entero esta precedido por un código 0O o 0o (cero-o), 
el número será tratado como un valor octal. Esto significa que el número debe contener dígitos en el rango del [0..7] únicamente.

"""
print("El numero en Octal 123 es igual en decimal a: " ,end="")
print(0o123)#0o123 es un número octal con un valor (decimal) igual a 83
print()
print()
print()

"""La segunda convención nos permite utilizar números en hexadecimal. Dichos números deben ser precedidos por el prefijo 0x o 0X (cero-x)"""
print("Este numero en Hexa 123 es igual en decimal a: ",end="")
print(0x123)
print()
print()
print()
